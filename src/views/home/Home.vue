<script setup lang="ts">
import { defineComponent, ref, onMounted, computed, onUnmounted } from "vue"
import Swiper from "../../components/Swiper.vue"
import EasyTyper from "easy-typer-js"
import MarkdownIt from "markdown-it"
import { usePagination } from "@/hooks/usePagination"
import { findArticleHomeListApi } from "@/api/article"
import { findTalkListApi } from "@/api/talk"
import { ArticleHome, Talk } from "@/api/types"
import { useWebStoreHook } from "@/stores/modules/website"
import { storeToRefs } from "pinia"

const { paginationData, handleCurrentChange, handleSizeChange } = usePagination()

// 获取存储的博客信息
const webStore = useWebStoreHook()

// 数据响应式
const showTips = ref(false)
// 网站运行时间
const time = ref("")
// 一言
const obj = ref({
  output: "",
  isEnd: false,
  speed: 300,
  singleBack: false,
  sleep: 0,
  type: "rollback",
  backSpeed: 40,
  sentencePause: true,
})

// 诗篇名称
const title = ref("")

// 作者
const author = ref("")

// 说说列表
const talkList = ref<Talk[]>([])
// 文章列表
const articleList = ref<ArticleHome[]>([])

// 初始化
const init = () => {
  document.title = webStore.blogInfo.websiteConfig.websiteName
  // 一言Api进行打字机循环输出效果
  getHitokoto()

  // 获取首页说说
  listHomeTalks()

  // 获取首页文章
  listHomeArticles()
}

interface Hitokoto {
  id: number
  uuid: string
  hitokoto: string
  type: string
  from: string
  from_who: string
  creator: string
  creator_uid: number
  reviewer: number
  commit_from: string
  created_at: string
  length: number
}

const getHitokoto = () => {
  fetch("https://v1.hitokoto.cn?c=i")
    .then((res) => {
      return res.json()
    })
    .then((res: Hitokoto) => {
      author.value = res.from_who
      title.value = res.from
      initTyped(res.hitokoto, null, null)
    })
}

const listHomeTalks = () => {
  findTalkListApi({
    page: 0,
    page_size: 10,
  }).then((res) => {
    talkList.value = res.data.list
  })
}

const listHomeArticles = () => {
  findArticleHomeListApi({
    page: paginationData.currentPage,
    page_size: paginationData.pageSize,
  }).then((res) => {
    paginationData.total = res.data.total
    paginationData.pageSize = res.data.page_size
    const list = res.data.list
    if (list.length) {
      // 去除markdown标签
      list.forEach((item) => {
        item.article_content = new MarkdownIt()
          .render(item.article_content)
          .replace(/<\/?[^>]*>/g, "")
          .replace(/[|]*\n/, "")
          .replace(/&npsp;/gi, "")
      })
      articleList.value.push(...list)
    }
  })
}

const initTyped = (input, fn, hooks) => {
  // obj.value.output = input
  const typed = new EasyTyper(obj.value, input, fn, function () {})
}

const scrollDown = () => {
  window.scrollTo({
    behavior: "smooth",
    top: document.documentElement.clientHeight,
  })
}

const runTime = () => {
  const timeold = new Date().getTime() - new Date(webStore.blogInfo.websiteConfig.websiteCreateTime).getTime()
  const msPerDay = 24 * 60 * 60 * 1000
  const daysold = Math.floor(timeold / msPerDay)
  let str = ""
  const day = new Date()
  str += daysold + "天"
  str += day.getHours() + "时"
  str += day.getMinutes() + "分"
  str += day.getSeconds() + "秒"
  time.value = str
}

// 计算属性
const isRight = computed(() => {
  return function (index) {
    if (index % 2 == 0) {
      return "article-cover left-radius"
    }
    return "article-cover right-radius"
  }
})

const isShowSocial = computed(() => {
  return function (social) {
    return webStore.blogInfo.websiteConfig.socialUrlList.indexOf(social) != -1
  }
})

const cover = computed(() => {
  return webStore.getCover("home")
})

let timer
const swiper = ref()
// 在组件挂载时启动定时器
onMounted(() => {
  init()
  runTime()
  timer = setInterval(runTime, 1000)
  // 可以在这里访问子组件的属性
  const swiperComponent = swiper.value
  if (swiperComponent) {
    console.log(swiperComponent.hello)
  }
})

// 在组件卸载时清除定时器
onUnmounted(() => {
  clearInterval(timer)
})
</script>

